# Example of relational operator 
number1 = 5010
number2 = 50
answer = number1 == number2
print(answer)
answer = number1 != number2 
print(answer)
answer = number1 > number2
print(answer)
answer = number1 < number2
print(answer)
answer = number1 >= number2
print(answer)
answer = number1 <= number2
print(answer)